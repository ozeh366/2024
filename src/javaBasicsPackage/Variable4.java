package javaBasicsPackage;

public class Variable4 {

	public static void main(String[] args) {
		int x = 100; // defining a local variable
		
		//calling a variable for printing its value
		System.out.println(x); //100
		
		//calling a variable for initialization
		int y = x;
		System.out.println(y); //100
		
		int a = 5;
		System.out.println(a);
		float f = a;// print out numbers in decimal
		System.out.println(f);
		double c = a; // print out numbers in decimal
		System.out.println(c);
		

	}

}
