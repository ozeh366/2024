package javaBasicsPackage;

public class AssignmentOperator {

	public static void main(String[] args) {
		int a = 10;

		a++; // increment by 1
		System.out.println(a);//11
		
		a += 3;//increase by 3
		System.out.println(a);//14
		
		a *= 3;// multiply by 3
		System.out.println(a);//42
		
		a /= 3; //divide by 3
		System.out.println(a);//14

	}

}
